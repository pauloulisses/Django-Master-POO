# Operadores aritméticos
"""
+ Adição - Realiza a soma de ambos operandos

- subtração - Realiza subtração de ambos operandos0

* multiplicação - Realiza a multiplicação de ambos operandos

/ Divisão - Realiza divisão de ambos operandos

// Divisão inteira -  Realiza a divisão entre operados e parte decima de ambos operadores

% Módulo - Retorna o resto da divisão de ambos operadores

** Exponenciação - Retorna o resultado da elevação da potência pelo outro
"""

# adição
print(5 + 9) # saída 14

# subtração
print(5 - 9) # saida 4

# multiplicação
print(5 * 9) # saída 45

# divisão
print(5 / 9) # 0.55555556

# divisão inteira
print(5 // 9) # saída 0

# modulo - resto da divisão de ambos operadores
print(5 % 9) # sáida 5

# exponenciação
print(5 ** 9) # saída 1953125