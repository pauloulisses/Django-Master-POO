# Variáveis são espaços na memória do computador onde você guarda valores para usar depois no programa

nome = "Paulo"

# mudando valor da variável
nome = "Eberson"
print(nome)

# type - tipo
print(type(nome)) # str

# Tipos de variáveis

# str - string (text)
nome1 = "Paulo"
print(f"seu nome é {nome1}")

# int - inteiro (números inteiros)
idade = 22
print(f"Sua idade é {idade}")

# float - números decimais
altura = 1.75
print(f"Sua altura é {altura}")


# boolean - verdadeiro ou falso
verdade = True
falso = False
print(verdade)
print(falso)



