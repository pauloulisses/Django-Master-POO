# Inicia o contador em 0
contador = 0

# Enquanto o contador for menor que 20
while contador < 20:

    # Aumenta o contador de 1 em 1
    contador += 1

    # Se o contador for igual a 3
    if contador == 3:
        # Para o loop completamente
        break

    # Mostra o valor do contador
    print(contador)
