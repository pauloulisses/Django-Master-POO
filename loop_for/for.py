# Cria uma lista com vários números
# usa-se o for para intera listas
lista = [1, 2, 3, 4, 5]

# Para cada número dentro da lista
for numero in lista:

    # Mostra o número atual
    print(numero)

# lista [ ]
# cria uma lista com vários nomes
nomes = ["Paulo", "Chayenne", "Jesus"]

# para cada nome dentro da lista
for nome in nomes:
    # Mostra o número atual
    print(nome)

# range - quantas vezes que interar executar um bloco de codigo
for numero in range(1, 11):
    print(numero)
